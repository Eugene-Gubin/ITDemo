import Foundation
import SystemConfiguration

public class KNNetworkUtils {
    
    public class func isConnectedToNetwork() -> Bool {
        
        var socketAddress = sockaddr_in(sin_len: 0, sin_family: 0, sin_port: 0, sin_addr: in_addr(s_addr: 0), sin_zero: (0, 0, 0, 0, 0, 0, 0, 0))
        socketAddress.sin_len = UInt8(sizeofValue(socketAddress))
        socketAddress.sin_family = sa_family_t(AF_INET)
        
        let reachability = withUnsafePointer(&socketAddress) {
            SCNetworkReachabilityCreateWithAddress(nil, UnsafePointer($0)).takeRetainedValue()
        }
        
        var flags: SCNetworkReachabilityFlags = 0
        if SCNetworkReachabilityGetFlags(reachability, &flags) == 0 {
            return false
        }
        
        let isReachable = (flags & UInt32(kSCNetworkFlagsReachable)) != 0
        let needsConnection = (flags & UInt32(kSCNetworkFlagsConnectionRequired)) != 0
        
        return isReachable && !needsConnection
    }
}